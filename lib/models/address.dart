class Address {
  final int? addressId;
  final String? address;
  final String? country;
  final String? state;
  final String? city;
  final String? postalCode;

  Address({
    required this.addressId,
    required this.address,
    required this.country,
    required this.state,
    required this.city,
    required this.postalCode,
  });
}
